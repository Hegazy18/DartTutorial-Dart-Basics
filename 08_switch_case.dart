void main() {
  String grade= 'C';

  switch (grade) {
    case 'A':
      print('Excellent');
      break;
    case 'B':
      print('Very good');
      break;
    case 'C':
      print('Good');
      break;
    case 'F':
      print('Failed');
      break;
    default:
    print("Invalid Grade");
  }
}
